# Global variables
ARGS="$@"
BASEDIR="$(pwd)"
BINDIR="$BASEDIR/bin"
SYSTEM="${SYSTEM:-bare_metal}"

# Flags
creation_flag=false
update_flag=false
destruction_flag=false
external_flag=false
